package com.example;

public class StudentReportsDetails {
	
	private int studentId;
	private int phase;
	private String scoreType;
	private int attentionIndex;
	private int engagementIndex;
	private int performanceIndex;
	public int getStudentId() {
		return studentId;
	}
	public void setStudentId(int studentId) {
		this.studentId = studentId;
	}
	public int getPhase() {
		return phase;
	}
	public void setPhase(int phase) {
		this.phase = phase;
	}
	public String getScoreType() {
		return scoreType;
	}
	public void setScoreType(String scoreType) {
		this.scoreType = scoreType;
	}
	public int getAttentionIndex() {
		return attentionIndex;
	}
	public void setAttentionIndex(int attentionIndex) {
		this.attentionIndex = attentionIndex;
	}
	public int getEngagementIndex() {
		return engagementIndex;
	}
	public void setEngagementIndex(int engagementIndex) {
		this.engagementIndex = engagementIndex;
	}
	public int getPerformanceIndex() {
		return performanceIndex;
	}
	public void setPerformanceIndex(int performanceIndex) {
		this.performanceIndex = performanceIndex;
	}
	
}
